# Boas práticas no Python 

# 1. Use nomes de variáveis com o uso do snake_case e com as 
#    letras minúsculas (Ex.: nome_variavel)

# 2. Siga o padrão de indentação (4 espaços por nível)

# 3. Mantenha linhas com no máximo 79 caracteres

# 4. Utilize docstrings para documentar funções e módulos

# 5. Evite códigos duplicados (DRY - Don't Repeat Yourself)

# 6. Use f-strings para formatação de strings

# 7. Importe apenas o necessário e organize os imports (PEP8)

# 8. Trate exceções de forma específica

# 9. Utilize comentários apenas quando necessário e seja claro

# 10. Mantenha o código limpo e organizado

# 11. Evite variáveis globais

# 12. Separe funções e responsabilidades (funções pequenas e coesas)